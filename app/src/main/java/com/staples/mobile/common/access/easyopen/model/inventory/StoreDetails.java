package com.staples.mobile.common.access.easyopen.model.inventory;

public class StoreDetails {

        private String address;
        private boolean available;
        private String city;
        private String country;
        private String faxNumber;
        private String latitude;
        private String longitude;
        private String phoneNumber;
        private String postalCode;
        private String saturdayHours;
        private String state;
        private String storeNumber;
        private String sundayHours;
        private String weekdayHours;

        public String getAddress() {
            return address;
        }

        public void setAddress(String address) {
            this.address = address;
        }

        public boolean getAvailable() {
            return available;
        }

        public void setAvailable(boolean available) {
            this.available = available;
        }

        public String getCity() {
            return city;
        }

        public void setCity(String city) {
            this.city = city;
        }

        public String getCountry() {
            return country;
        }

        public void setCountry(String country) {
            this.country = country;
        }

        public String getFaxNumber() {
            return faxNumber;
        }

        public void setFaxNumber(String faxNumber) {
            this.faxNumber = faxNumber;
        }

        public String getLatitude() {
            return latitude;
        }

        public void setLatitude(String latitude) {
            this.latitude = latitude;
        }

        public String getLongitude() {
            return longitude;
        }

        public void setLongitude(String longitude) {
            this.longitude = longitude;
        }

        public String getPhoneNumber() {
            return phoneNumber;
        }

        public void setPhoneNumber(String phoneNumber) {
            this.phoneNumber = phoneNumber;
        }

        public String getPostalCode() {
            return postalCode;
        }

        public void setPostalCode(String postalCode) {
            this.postalCode = postalCode;
        }

        public String getSaturdayHours() {
            return saturdayHours;
        }

        public void setSaturdayHours(String saturdayHours) {
            this.saturdayHours = saturdayHours;
        }

        public String getState() {
            return state;
        }

        public void setState(String state) {
            this.state = state;
        }

        public String getStoreNumber() {
            return storeNumber;
        }

        public void setStoreNumber(String storeNumber) {
            this.storeNumber = storeNumber;
        }

        public String getSundayHours() {
            return sundayHours;
        }

        public void setSundayHours(String sundayHours) {
            this.sundayHours = sundayHours;
        }

        public String getWeekdayHours() {
            return weekdayHours;
        }

        public void setWeekdayHours(String weekdayHours) {
            this.weekdayHours = weekdayHours;
        }

}
