public class Reward {

    private String amountRewards;

    private String rewardsEndDate;

    private String rewardsMessage;

    private String rewardsPromoImage;

    private String rewardsStartDate;

    private String rewardsTotalAmount;

    public String getRewardsTotalAmount() {
        return rewardsTotalAmount;
    }

    public void setRewardsTotalAmount(String rewardsTotalAmount) {
        this.rewardsTotalAmount = rewardsTotalAmount;
    }

    public String getAmountRewards() {
        return amountRewards;
    }

    public void setAmountRewards(String amountRewards) {
        this.amountRewards = amountRewards;
    }

    public String getRewardsEndDate() {
        return rewardsEndDate;
    }

    public void setRewardsEndDate(String rewardsEndDate) {
        this.rewardsEndDate = rewardsEndDate;
    }

    public String getRewardsMessage() {
        return rewardsMessage;
    }

    public void setRewardsMessage(String rewardsMessage) {
        this.rewardsMessage = rewardsMessage;
    }

    public String getRewardsPromoImage() {
        return rewardsPromoImage;
    }

    public void setRewardsPromoImage(String rewardsPromoImage) {
        this.rewardsPromoImage = rewardsPromoImage;
    }

    public String getRewardsStartDate() {
        return rewardsStartDate;
    }

    public void setRewardsStartDate(String rewardsStartDate) {
        this.rewardsStartDate = rewardsStartDate;
    }
}
