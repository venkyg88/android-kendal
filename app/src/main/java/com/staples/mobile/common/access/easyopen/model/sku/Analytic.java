package com.staples.mobile.common.access.easyopen.model.sku;

public class Analytic {
    private String categoryCode;
    private String categoryName;
    private String classCode;
    private String className;
    private String departmentCode;
    private String departmentName;
    private String partNumber;
    private String superCategoryCode;
    private String superCategoryName;

    public String getCategoryCode() {
        return categoryCode;
    }

    public String getCategoryName() {
        return categoryName;
    }

    public String getClassCode() {
        return classCode;
    }

    public String getClassName() {
        return className;
    }

    public String getDepartmentCode() {
        return departmentCode;
    }

    public String getDepartmentName() {
        return departmentName;
    }

    public String getPartNumber() {
        return partNumber;
    }

    public String getSuperCategoryCode() {
        return superCategoryCode;
    }

    public String getSuperCategoryName() {
        return superCategoryName;
    }
}
